extension CollectionType where Self.Generator.Element == Int8 {
    public var hexString: String {
        var string = ""
        for (index, value) in self.enumerate() {
            if index % 2 == 0 && index > 0 {
                string += " "
            }
            string += (value < 16 ? "0" : "") + String(value, radix: 16)
        }
        return string
    }
}